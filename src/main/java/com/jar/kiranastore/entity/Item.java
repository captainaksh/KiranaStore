package com.jar.kiranastore.entity;

public class Item {

	private String itemName;
	private int rateUSD;
	private String rateINR;
	
	public String getItemName() {
		return itemName;
	}
	public void setItemName(String itemName) {
		this.itemName = itemName;
	}
	public int getRateUSD() {
		return rateUSD;
	}
	public void setRateUSD(int rateUSD) {
		this.rateUSD = rateUSD;
	}
	public String getRateINR() {
		return rateINR;
	}
	public void setRateINR(String rateINR) {
		this.rateINR = rateINR;
	}
	
}
